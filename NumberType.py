# 사칙연산
num1 = 5
num2 = 7
print(num1 + num2)  # +
print(num1 - num2)  # -
print(num1 * num2)  # *
print(num1 / num2)  # /

# 제곱 연산
print(num1 ** num2)

# 나머지 연산
print(num1 % num2)

# 몫 반환 연산자
print(num1 // num2)